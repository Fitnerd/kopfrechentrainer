package com.example.enno.rechnentrainier;


public class Task {
    int operand1;
    int operand2;
    int operator;
    int result;

    public Task(int pOperand1, int pOperand2, int pOperator, int pResult ) {
       operand1 = pOperand1;
       operand2 = pOperand2;
       operator = pOperator;
       result = pResult; 
    }

    public getOperand1 {
        return operand1;
    }
    public getOperand2 {
        return operand2;
    }
    public getOperator {
        return operator;
    }
    public getResult {
        return result;
    }
}
